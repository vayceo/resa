    @JvmStatic
    fun getName(skinId: Int): String {
        if(" " == list[skinId].name)
            return "Скин $skinId"

        return list[skinId].name
    }

    fun getRare(skinId: Int): Int {
        return Rare.getRareFromPrice(getPriceLC(skinId))
    }

    fun getPriceRub(skinId: Int): Int {
        return list[skinId].priceRub
    }

    private fun getXOffset(skinId: Int): Float {
        return list[skinId].xOffset
    }

    private fun getYOffset(skinId: Int): Float {
        return list[skinId].yOffset
    }

    private fun getZOffset(skinId: Int): Float {
        return list[skinId].zOffset
    }

    @JvmStatic
    fun getPriceLC(skinId: Int): Int {
        return list[skinId].priceLC
    }

    fun getSnap(modelId: Int): SnapShot {
        return SnapShot(TYPE_PED, modelId, 0f, 180f, 0f, getXOffset(modelId), getYOffset(modelId), getZOffset(modelId))
    }

    fun createDonateItem(modelId: Int): DonateItem {
        return DonateItem(
                getName(modelId),
                Donate.CATEGORY_SKINS,
                getPriceLC(modelId),
                modelId,
                getSnap(modelId)
            )
    }

    fun createTreasureItem(modelId: Int): TreasureItem {
        return TreasureItem(
            SnapShot(TYPE_PED, modelId, 0f, 180f, 0f, getXOffset(modelId), getYOffset(modelId), getZOffset(modelId)),
            getName(modelId),
            getRare(modelId)
        )
    }
